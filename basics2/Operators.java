package basics2;

public class Operators {
    public static void main(String[] args) {
        int a = 12;
        int b = 7;
        double c = (double) a / b;
        double d = a / (double) b;
        a += b;
        boolean e = a != b;
        boolean f = a == b;

        System.out.println(d);
        System.out.println(c);
        System.out.println(a);
        System.out.println(e);
        System.out.println(f);
        
    }

}
   
